package week1homework1;

public class PrintingValuesOfDataTypes {

	public static void main(String[] args) {
		int aInt = 5;
		float bFloat = 7.45f;
		char cChar = 'G';
		double dDouble = 4.3D;
		long lLong = 45678;
		short sShort = 67;
		boolean isBoolean = true;
		byte bByte = 2;	
		
		System.out.println("Integer:"+aInt);
		System.out.println("Float:"+bFloat);
		System.out.println("Character:"+cChar);
		System.out.println("Double:"+dDouble);
		System.out.println("Long:"+lLong);
		System.out.println("Short:"+sShort);
		System.out.println("Boolean:"+isBoolean);
		System.out.println("Byte:"+bByte);

	}

}
